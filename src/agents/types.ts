/**
 * Richer input shape for the Preprocess Agent. Declared as a local
 * extension of `AgentInput` so the existing `Agent` interface can stay
 * untouched (declaration merging is not required). The agent's
 * `run(input: PreprocessInput)` accepts this shape directly.
 */
export interface PreprocessInput {
  image_id: ImageId;
  /** Raw image bytes; required for MD5/pHash/metadata extraction. */
  image_bytes: Buffer;
  /** Optional upstream context (unused in Sprint 3; reserved for epic-2). */
  context?: Readonly<Record<string, unknown>>;
}

/**
 * Metadata block returned by the Preprocess Agent on the `details`
 * field. The shape is a superset of the `sharp().metadata()` result
 * plus a `source` discriminator so callers can tell sharp from manual.
 */
export interface PreprocessMetadata {
  width?: number;
  height?: number;
  format?: string;
  space?: string;
  channels?: number;
  hasAlpha?: boolean;
  /** Discriminator: "sharp" (real) or "manual" (header parser). */
  source?: string;
  /** Index signature so the shape is assignable to `Record<string, unknown>`. */
  [key: string]: unknown;
}

/**
 * The `details` payload the Preprocess Agent attaches to its
 * `AgentOutput`. The fields are additive — future epics may extend
 * without breaking the SC-1/2/3 contract.
 */
export interface PreprocessOutputDetails {
  width: number;
  height: number;
  format: "png" | "jpeg" | "webp" | "unknown";
  hash: string;
  phash?: string;
  normalized_bytes?: Buffer;
  metadata: PreprocessMetadata;
  /** Which path the agent used for metadata + normalization. */
  fallback: "sharp" | "manual";
  /** Whether the existing image-compressor.ts was actually invoked. */
  image_compressor_used?: boolean;
  /** Error message from image-compressor if it failed (best-effort). */
  image_compressor_error?: string | null;
  /** Set when the blocklist fast-path fired. */
  hit_kind?: "md5" | "phash";
  /** The matched hash (MD5 or pHash) when the blocklist fired. */
  hit_hash?: string;
  /** Index signature so the shape is assignable to `Record<string, unknown>`. */
  [key: string]: unknown;
}

/**
 * Strongly-typed alias for the Preprocess Agent output shape.
 * (The actual return is `AgentOutput`; this alias is for documentation
 * and for downstream consumers that want to narrow `details`.)
 */
export type PreprocessOutput = AgentOutput & {
  details?: PreprocessOutputDetails;
};

/** A single blocklist entry (MD5 or pHash). */
export interface BlocklistEntry {
  hash: string;
  kind: "md5" | "phash";
}

/**
 * Payload of the `pipeline.preprocess.blocklist-hit` bus event.
 * Published on the active MessageBus by the Preprocess Agent when the
 * blocklist fast-path fires.
 */
export interface BlocklistHitEvent {
  image_id: ImageId;
  /** The matched MD5 (when `hit_kind === "md5"`). */
  md5?: string;
  /** The matched pHash (when `hit_kind === "phash"`). */
  phash?: string;
  hit_kind: "md5" | "phash";
  /** ISO timestamp the event was published. */
  publishedAt: string;
}

