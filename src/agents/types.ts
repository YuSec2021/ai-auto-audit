/**
 * The four specialized sub-targets a single image is evaluated against.
 * Discriminator carried on `SpecializedInput.target` and on
 * `SpecializedOutput.details.target`.
 */
export type SpecializedTarget = "porn" | "ad" | "political" | "logo";

/**
 * The richer input shape for any specialized sub-agent.
 * Re-declared here (alongside the local declaration in
 * src/specialized/specialized-types.ts) so consumers that only import
 * from `@/agents` see the type.
 */
export type SpecializedInput = AgentInput & {
  /** Which specialized target this invocation is scoring. */
  target: SpecializedTarget;
};

/**
 * The richer output shape for any specialized sub-agent.
 */
export type SpecializedOutput = AgentOutput & {
  details: {
    target: SpecializedTarget;
    [key: string]: unknown;
  };
};

/**
 * Action enum emitted by the Risk Fusion Agent. The mapping to a
 * score band is configurable via `FUSION_THRESHOLDS` /
 * `configureFusion({ thresholds })`.
 */
export type FusionAction = "PASS" | "REVIEW" | "REJECT";

/**
 * The 7 per-layer score keys. Each layer contributes to the weighted
 * sum per the value of `FUSION_WEIGHTS[k]`.
 */
export type FusionWeights = {
  text_risk: number;
  vision: number;
  metadata: number;
  porn: number;
  ad: number;
  political: number;
  logo: number;
};

/**
 * The two threshold boundaries the Risk Fusion Agent uses to map a
 * weighted sum to an action:
 *   - score >= rejectAt → "REJECT"
 *   - score >= reviewAt → "REVIEW"
 *   - else            → "PASS"
 */
export type FusionThresholds = {
  rejectAt: number;
  reviewAt: number;
};

/**
 * The richer input shape for the Risk Fusion Agent. The `per_layer`
 * record carries the 7 per-layer scores; missing entries default to
 * 0.0 (see `risk-fusion-agent.ts`).
 */
export type FusionInput = AgentInput & {
  context: {
    per_layer: Partial<FusionWeights>;
  };
};

/**
 * The richer output shape for the Risk Fusion Agent.
 */
export type FusionOutput = AgentOutput & {
  details: {
    action: FusionAction;
    per_layer: Readonly<FusionWeights>;
    weights: Readonly<FusionWeights>;
  };
};

/**
 * Declaration-merge interface for the 4 OPTIONAL specialized slots
 * added in Sprint 4. The orchestrator's local slot type widens to
 * `OrchestratorSlots & Partial<SpecializedOrchestratorSlots>`, so
 * the public 5-field shape in `OrchestratorSlots` is preserved
 * (callers from Sprint 1-3 keep compiling) while Sprint 4 callers
 * can opt-in to the 4 new slots.
 *
 * See sprint-contract.md Risks §1 for the deviation rationale.
 */
export interface SpecializedOrchestratorSlots {
  /** Slot for the Porn-Agent (target: "porn"). */
  porn?: import("./types.js").Agent;
  /** Slot for the Ad-Agent (target: "ad"). */
  ad?: import("./types.js").Agent;
  /** Slot for the Political-Agent (target: "political"). */
  political?: import("./types.js").Agent;
  /** Slot for the Logo-Agent (target: "logo"). */
  logo?: import("./types.js").Agent;
}
