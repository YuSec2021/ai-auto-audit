/**
 * Sprint 1 — Agent runtime type definitions.
 *
 * The types in this file define the contract every agent must satisfy
 * (Agent, AgentInput, AgentOutput, AgentContext, HealthStatus), the
 * transport-spanning MessageBus abstraction, the typed AgentMessage
 * envelope that flows over the bus, and the AuditLogEntry shape that
 * gets persisted to ./logs/audit_*.log after every agent.run() call.
 *
 * Defaults (per sprint-contract.md open questions):
 *   - `Agent` is declared as a TypeScript `interface` (declaration-merging friendly).
 *   - `image_id` is a plain `string` (no branded type) for Sprint 1.
 *   - `HealthStatus` is a discriminated union:
 *       { ok: true;  latencyMs: number } | { ok: false; error: string }
 *   - `MessageBus.request<TReq, TRes>(type, payload, timeoutMs?)` returns Promise<TRes>.
 */

/** A free-form identifier for an image flowing through the pipeline. */
export type ImageId = string;

/** A free-form identifier for an audit run / task. */
export type RunId = string;

/** Correlation id generated by MessageBus.request() to wire request/response. */
export type CorrelationId = string;

/**
 * Discriminated union health report.
 *   - `ok: true`  → the agent is ready; `latencyMs` is a self-measured readiness probe.
 *   - `ok: false` → the agent is degraded or unusable; `error` is a human-readable cause.
 */
export type HealthStatus =
  | { ok: true; latencyMs: number }
  | { ok: false; error: string };

/** Static, immutable context handed to an agent at init() time. */
export interface AgentContext {
  /** Optional human-readable run id (orchestrator assigns it). */
  runId?: RunId;
  /** Optional directory the agent may use for caching or scratch. */
  workDir?: string;
  /** Free-form configuration key/value bag (e.g. model name, thresholds). */
  config?: Readonly<Record<string, unknown>>;
}

/** Per-invocation input passed to Agent.run(). */
export interface AgentInput {
  image_id: ImageId;
  /** Image bytes; transport is opaque to the bus. May be a Buffer, base64 string, or remote URL. */
  image?: Buffer | string;
  /** Additional structured context (e.g. upstream OCR text, EXIF, prior layer scores). */
  context?: Readonly<Record<string, unknown>>;
}

/** Per-invocation output produced by Agent.run(). */
export interface AgentOutput {
  image_id: ImageId;
  /** Numeric risk / confidence score in [0, 1] (or any comparable range the agent documents). */
  score: number;
  /** Short human-readable reason / category. */
  reason: string;
  /** Optional extra structured fields the agent wants to publish. */
  details?: Readonly<Record<string, unknown>>;
}

/**
 * The contract every agent (Preprocess, OCR, Text Risk, Vision, Metadata,
 * Porn, Ad, Political, Logo, Risk Fusion) must implement.
 *
 * Declared as `interface` so downstream code can declaration-merge it for
 * agent-specific extensions without rewrites.
 */
export interface Agent {
  readonly id: string;
  readonly version: string;
  init(ctx: AgentContext): Promise<void>;
  run(input: AgentInput): Promise<AgentOutput>;
  healthcheck(): Promise<HealthStatus>;
}

/**
 * Typed envelope carried over the MessageBus.
 *   - `type`    is the channel key (e.g. "agent.preprocess.done").
 *   - `payload` is the user-defined body.
 *   - `correlationId` is set by `request()` and echoed back in the response
 *                    message; absent on ordinary `publish()` fan-out.
 */
export interface AgentMessage<T = unknown> {
  type: string;
  payload: T;
  correlationId?: CorrelationId;
  /** ISO timestamp the message was published. */
  publishedAt: string;
}

/** Subscriber callback signature. */
export type MessageHandler<T = unknown> = (msg: AgentMessage<T>) => void | Promise<void>;

/** Unsubscribe handle returned by MessageBus.subscribe(). */
export type Unsubscribe = () => void;

/**
 * Transport-agnostic message bus contract.
 *
 * Sprint 1 ships a single in-process implementation
 * (`EventEmitterMessageBus` in ./message-bus.ts). A future NATS/Kafka
 * transport can satisfy the same interface without touching agent code.
 */
export interface MessageBus {
  /**
   * Fire-and-forget broadcast. Returns the number of subscribers that received it.
   * The optional `correlationId` is included in the AgentMessage envelope so
   * that `request()` responders can echo it back on `<type>.response`.
   */
  publish<T>(type: string, payload: T, correlationId?: CorrelationId): number;

  /**
   * Subscribe to a message type. The handler may be sync or async.
   * EventEmitter semantics: handlers run in registration order; a slow
   * handler will block later subscribers (see Risks in sprint-contract.md).
   */
  subscribe<T>(type: string, handler: MessageHandler<T>): Unsubscribe;

  /**
   * Request/response round-trip on the bus. Auto-generates a correlation
   * id, publishes a request, awaits a response on `type + ".response"`,
   * and resolves with the responding handler's return value. Rejects on
   * timeout (default 5000 ms) or if the responder throws.
   */
  request<TReq, TRes>(
    type: string,
    payload: TReq,
    timeoutMs?: number,
  ): Promise<TRes>;
}

/** One structured line appended to ./logs/audit_*.log per agent.run() call. */
export interface AuditLogEntry {
  image_id: ImageId;
  agent: string;
  score: number;
  reason: string;
  elapsed_ms: number;
}

