/**
 * The state machine states. Duplicated here as a string-literal union
 * (re-declared from `src/orchestrator/state-machine.ts`) so consumer code
 * that only imports from `@/agents` does not need to take a dependency
 * on the orchestrator module. The orchestrator module's table is the
 * authoritative adjacency; this union is the type-level alias.
 */
export type PipelineStateS2 =
  | "init"
  | "preprocess"
  | "parallel-fan-out"
  | "fan-in"
  | "fusion"
  | "done"
  | "cancelled"
  | "failed";

/**
 * Envelope published on the MessageBus by the orchestrator for every
 * state transition and for terminal events.
 */
export interface PipelineEvent {
  /** The state the event is about. */
  state: PipelineStateS2;
  /** Image id this transition belongs to. */
  image_id: ImageId;
  /** ISO timestamp the event was published. */
  publishedAt: string;
  /** Optional human-readable reason (e.g. cancel reason, error message). */
  reason?: string;
  /** Free-form extra context the subscriber may want. */
  context?: Readonly<Record<string, unknown>>;
}

/**
 * A checkpoint record the orchestrator publishes on
 * `pipeline.checkpoint` and stores in memory for `resume()`. Sprint 2
 * keeps the checkpoint in memory only; Sprint 3+ may add file-backed
 * durability without changing the public surface.
 */
export interface PipelineCheckpoint {
  /** Image id the checkpoint belongs to. */
  image_id: ImageId;
  /** The state the pipeline was last in before pausing/resuming. */
  lastState: PipelineStateS2;
  /** Output of the preprocess agent, if it has run. */
  preprocessOutput?: import("./types.js").AgentOutput;
  /** Output of the three fan-out agents, keyed by slot name. */
  fanOutOutputs?: {
    textRisk?: import("./types.js").AgentOutput;
    vision?: import("./types.js").AgentOutput;
    metadata?: import("./types.js").AgentOutput;
  };
  /** Output of the fusion agent, if it has run. */
  fusionOutput?: import("./types.js").AgentOutput;
  /** ISO timestamp the checkpoint was written. */
  publishedAt: string;
}

/**
 * Named agent slots the orchestrator consumes. Sprint 2 ships 1-line
 * stubs for `preprocess` and `fusion`; epic-2/3/4/6 will replace them.
 * All slots implement the Sprint 1 `Agent` interface, so any concrete
 * agent — stub or real — can be passed in via `init(slots)`.
 */
export interface OrchestratorSlots {
  preprocess: import("./types.js").Agent;
  textRisk: import("./types.js").Agent;
  vision: import("./types.js").Agent;
  metadata: import("./types.js").Agent;
  fusion: import("./types.js").Agent;
}

/** The minimal input the orchestrator's `run()` accepts. */
export interface OrchestratorInput {
  image_id: ImageId;
  /** Optional raw image bytes; threaded through to the metadata agent. */
  image_bytes?: Buffer;
  /** Optional pre-existing OCR text; threaded through to text-risk + vision. */
  ocr_text?: string;
  /** Optional pre-existing bounding boxes from OCR. */
  bboxes?: ReadonlyArray<unknown>;
  /** Optional EXIF summary, threaded through to the vision agent. */
  exif_summary?: Readonly<Record<string, unknown>>;
}

/**
 * Per-layer outputs after a successful pipeline run.
 * Strongly-typed (per sprint-contract.md open question #4) so epic-6's
 * real Risk Fusion has a typed input.
 */
export interface FanInResult {
  preprocess: import("./types.js").AgentOutput;
  textRisk: import("./types.js").AgentOutput;
  vision: import("./types.js").AgentOutput;
  metadata: import("./types.js").AgentOutput;
}

/** Subscriber callback for orchestrator state-change events. */
export type OrchestratorSubscriber = (event: PipelineEvent) => void;

